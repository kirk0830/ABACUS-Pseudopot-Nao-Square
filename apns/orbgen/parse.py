import re
def parse(fname: str):
    """parse ABACUS numerical atomic orbital files, a dict will be returned.
    Following keys are supported:
    
    - element: str, the element of the atom
    - mesh: int, the number of mesh points
    - dr: float, the step size of mesh
    - ecutwfc: float, the energy cutoff
    - rcut: float, the radius cutoff
    - lmax: int, the maximum angular momentum
    - nzeta: list, the number of zeta orbitals for each angular momentum
    - data: list, the numerical orbitals, the first index is the angular momentum, the second index is the zeta orbital index, the value is a list of mesh points.
    """
    result = {"data": []}
    spectrum = ["S", "P", "D", "F", "G", "H", "I"]

    with open(fname, "r") as f:
        lines = f.readlines()
    lines = [line.strip() for line in lines]
    lines = [line for line in lines if line != ""]
    
    read_header = False
    it, l, izeta = 0, 0, 0
    for line in lines:
        if re.match(r"^-+$", line): # the line is a separator
            read_header = True if read_header == False else False
            continue
        if read_header:
            key, value = line.rsplit(" ", 1)
            result.update({key.replace("-->", "").strip(): value.strip()})
            continue
        else:
            if re.match(r"^SUMMARY\s+END$", line):
                result["data"] = [[
                    [] for j in range(int(result[f"Number of {spectrum[i]}orbital"]))
                ] for i in range(int(result["Lmax"]) + 1)]
                continue
            if re.match(r"^[\w]+\s+[\d\.]+$", line):
                key, value = line.rsplit(" ", 1)
                result.update({key.strip(): value.strip()})
                continue
            if re.match(r"^Type\s+L\s+N$", line):
                continue
            if re.match(r"^\d\s+\d\s+\d", line):
                it, l, izeta = map(int, line.split())
                continue
            if re.match(r"^([+-]?\d+(\.\d+e[+-]?\d+)?\s+)?([+-]?\d+(\.\d+e[+-]?\d+)?)", line):
                result["data"][l][izeta].extend([float(x) for x in line.split()])
                continue
            print(f"line not parsed: {line}")
    
    result["element"] = result["Element"].strip().lower().capitalize()
    del result["Element"]
    result["ngrid"] = int(result["Mesh"])
    del result["Mesh"]
    result["dr"] = float(result["dr"])
    result["ecutwfc"] = float(result["Energy Cutoff(Ry)"])
    del result["Energy Cutoff(Ry)"]
    result["rcut"] = float(result["Radius Cutoff(a.u.)"])
    del result["Radius Cutoff(a.u.)"]
    result["lmax"] = int(result["Lmax"])
    del result["Lmax"]
    result["nzeta"] = [int(result[f"Number of {spectrum[i]}orbital"]) for i in range(result["lmax"] + 1)]
    for i in range(result["lmax"] + 1):
        del result[f"Number of {spectrum[i]}orbital"]
    return result

